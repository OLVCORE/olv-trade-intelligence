/**
 * Busca porto por código UN/LOCODE
 */
export function getPortByCode(code: string): Port | undefined {
  return PORTS.find((p) => p.code === code);
}

/**
 * Busca portos por nome, país ou código
 */
export function searchPorts(query: string): Port[] {
  const q = query.toLowerCase();
  return PORTS.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.code.toLowerCase().includes(q) ||
      p.country.toLowerCase().includes(q)
  );
}

/**
 * Busca portos por código de país
 */
export function getPortsByCountry(countryCode: string): Port[] {
  return PORTS.filter((p) => p.countryCode === countryCode);
}

/**
 * Busca portos por região
 */
export function getPortsByRegion(region: string): Port[] {
  return PORTS.filter((p) => p.region === region);
}

/**
 * Busca portos por tipo
 */
export function getPortsByType(type: 'ocean' | 'air' | 'both'): Port[] {
  return PORTS.filter((p) => p.type === type || p.type === 'both');
}

/**
 * Valida se código de porto é válido (formato: 2 letras país + 3 letras localização)
 */
export function isValidPortCode(code: string): boolean {
  return /^[A-Z]{2}[A-Z0-9]{3}$/.test(code.toUpperCase());
}

